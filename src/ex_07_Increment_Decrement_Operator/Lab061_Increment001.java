package ex_07_Increment_Decrement_Operator;

public class Lab061_Increment001 {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);


        // Line No. |   a | Exp(result)
        //  5   | 10 | NA
        // 6 | 11 | 11
    }
}
