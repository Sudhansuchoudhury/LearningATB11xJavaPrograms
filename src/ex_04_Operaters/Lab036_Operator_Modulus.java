package ex_04_Operaters;

public class Lab036_Operator_Modulus {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b);
        // Modulus -> Remainder,
//        10 | 20 |  2 - Quoinet  //Here 2 is Quoinet
//           | 20 |
//           -------
//             0 - Remainder    / Always the result will be the remainder value
//           ----

        System.out.println(13%7); //Here the answer will be 6
}}
