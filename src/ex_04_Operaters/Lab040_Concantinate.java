package ex_04_Operaters;

public class Lab040_Concantinate {
    public static void main(String[] args) {
        // concatination
//        System.out.println("X"+ "Y");

        int a = 10;
        int b = 20;
        System.out.println(a + b); //  // Math

        String name1 = "X";
        String name2 = "Y";
        System.out.println(name1+name2);

        // + -> behave differently with the data type.
        // + -> operator overloading
        // Int data type's output will be 30
        // Where for String data type the value will be X+Y
    }

}
