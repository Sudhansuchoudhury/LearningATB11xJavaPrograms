package ex_04_Operaters;

public class Lab037_Relational_Operators$Greater$Less {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c); //The result will be in true or false

        int x = 33;
        int y= 37;
        boolean result = y >= x;
        System.out.println(result);

        // y > x or y = x
    }

}
