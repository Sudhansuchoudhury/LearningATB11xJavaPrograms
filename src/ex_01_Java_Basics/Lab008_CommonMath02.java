package ex_01_Java_Basics;

public class Lab008_CommonMath02 {
    public static void main(String[] args) {
        int a=2;
        int b=4;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(b-a);
        System.out.println(b/a);
    }
}
